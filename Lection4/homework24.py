# Задача 24: В фермерском хозяйстве в Карелии выращивают чернику. Она растёт на круглой грядке, 
# причём кусты высажены только по окружности. Таким образом, у каждого куста есть ровно два соседних. 
# Всего на грядке растёт N кустов.
# Эти кусты обладают разной урожайностью, поэтому ко времени сбора на них выросло различное число ягод — на i-ом кусте выросло ai ягод.
# В этом фермерском хозяйстве внедрена система автоматического сбора черники. 
# Эта система состоит из управляющего модуля и нескольких собирающих модулей. 
# Собирающий модуль за один заход, находясь непосредственно перед некоторым кустом, собирает ягоды с этого куста и с двух соседних с ним.
# Напишите программу для нахождения максимального числа ягод, которое может собрать 
# за один заход собирающий модуль, находясь перед некоторым кустом заданной во входном файле грядки.

def max_berries(garden):
    n = len(garden)
    max_berries = 0

    # Обработка каждого куста черники
    for i in range(n):
        berries = garden[i] + garden[(i+1) % n] + garden[(i-1) % n]
        max_berries = max(max_berries, berries)

    return max_berries


# Чтение количества кустов черники
n = int(input("Введите количество кустов черники: "))

# Чтение урожайности каждого куста черники
garden = []
print("Введите урожайность каждого куста черники:")
for _ in range(n):
    berries = int(input())
    garden.append(berries)

# Вычисление максимального числа ягод
result = max_berries(garden)

# Вывод результата
print("Максимальное число ягод, которое может собрать собирающий модуль:", result)