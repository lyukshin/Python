# Задача No5. Решение в группах
# Вагоны в электричке пронумерованы натуральными числами, 
# начиная с 1 (при этом иногда вагоны нумеруются от «головы» поезда, 
# а иногда – с «хвоста»; это зависит от того, в какую сторону едет электричка). 
# В каждом вагоне написан его номер. Витя сел в i-й вагон от головы поезда и 
# обнаружил, что его вагон имеет номер j. Он хочет определить, 
# сколько всего вагонов в электричке. Напишите программу, которая будет 
# это делать или сообщать, что без дополнительной информации 
# это сделать невозможно.
# Input: 3 4(ввод на разных строках) Output: 6


def calculate_total_wagons(i, j):
    forward_wagons = j + i - 1  # количество вагонов при нумерации от головы поезда
    backward_wagons = i + j - 1  # количество вагонов при нумерации от хвоста поезда
    return max(forward_wagons, backward_wagons)

# Чтение вводных данных
i = int(input("Введите позицию вагона Вити: "))
j = int(input("Введите номер вагона Вити: "))

total_wagons = calculate_total_wagons(i, j)
print(f"В электричке всего {total_wagons} вагонов.")

